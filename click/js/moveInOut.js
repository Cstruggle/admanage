	function movein(tag){
		switch(tag)
		{   case 0:
			$("#ul0").show();
			break;

			case 1:
			$("#ul1").show();
			break;

			case 2:
			$("#ul2").show();
			break;

			case 3:
			$("#ul3").show();
			break;

			case 4:
			$("#ul4").show();
			break;
		}
	}

	function moveout(){
	
			$("#ul0").hide();

			$("#ul1").hide();

			$("#ul2").hide();

			$("#ul3").hide();

			$("#ul4").hide();
	}